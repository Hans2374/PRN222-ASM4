using Microsoft.EntityFrameworkCore;
using PaymentCVSTS.Repositories.Models;

namespace PaymentCVSTS.Repositories.Basic
{
    public class GenericRepository<T> where T : class
    {
        protected SP25_PRN222_NET1704_PRJ_G5_CVSTSContext _context;

        public GenericRepository()
        {
            _context = new SP25_PRN222_NET1704_PRJ_G5_CVSTSContext();
        }

        public GenericRepository(SP25_PRN222_NET1704_PRJ_G5_CVSTSContext context)
        {
            _context = context;
        }

        public List<T> GetAll()
        {
            return _context.Set<T>().ToList();
        }
        public async Task<List<T>> GetAllAsync()
        {
            return await _context.Set<T>().ToListAsync();
        }
        public void Create(T entity)
        {
            _context.Add(entity);
            _context.SaveChanges();
        }

        public async Task<int> CreateAsync(T entity)
        {
            _context.Add(entity);
            return await _context.SaveChangesAsync();
        }

        public void Update(T entity)
        {
            // Use Update instead of Attach to handle tracking conflicts
            _context.Update(entity);
            _context.SaveChanges();
        }

        public async Task<int> UpdateAsync(T entity)
        {
            // Create a new context for the update operation to avoid tracking conflicts
            using (var freshContext = new SP25_PRN222_NET1704_PRJ_G5_CVSTSContext())
            {
                freshContext.Update(entity);
                return await freshContext.SaveChangesAsync();
            }
        }

        public bool Remove(T entity)
        {
            _context.Remove(entity);
            _context.SaveChanges();
            return true;
        }

        public async Task<bool> RemoveAsync(T entity)
        {
            _context.Remove(entity);
            await _context.SaveChangesAsync();
            return true;
        }

        public T GetById(int id)
        {
            var entity = _context.Set<T>().Find(id);
            if (entity != null)
            {
                _context.Entry(entity).State = EntityState.Detached;
            }

            return entity;
        }

        public async Task<T> GetByIdAsync(int id)
        {
            var entity = await _context.Set<T>().FindAsync(id);
            if (entity != null)
            {
                _context.Entry(entity).State = EntityState.Detached;
            }

            return entity;
        }

        public T GetById(string code)
        {
            var entity = _context.Set<T>().Find(code);
            if (entity != null)
            {
                _context.Entry(entity).State = EntityState.Detached;
            }

            return entity;
        }

        public async Task<T> GetByIdAsync(string code)
        {
            var entity = await _context.Set<T>().FindAsync(code);
            if (entity != null)
            {
                _context.Entry(entity).State = EntityState.Detached;
            }

            return entity;
        }

        public T GetById(Guid code)
        {
            var entity = _context.Set<T>().Find(code);
            if (entity != null)
            {
                _context.Entry(entity).State = EntityState.Detached;
            }

            return entity;
        }

        public async Task<T> GetByIdAsync(Guid code)
        {
            var entity = await _context.Set<T>().FindAsync(code);
            if (entity != null)
            {
                _context.Entry(entity).State = EntityState.Detached;
            }

            return entity;
        }
    }
}